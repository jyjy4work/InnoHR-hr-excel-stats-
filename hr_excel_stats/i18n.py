def t(key: str, lang: str = "ko", **kwargs) -> str:
    """
    번역 함수.

    Args:
        key:    번역 키 (TRANSLATIONS 딕셔너리 키)
        lang:   언어 코드 ("ko" | "en" | "fr"), 기본값 "ko"
        **kwargs: 문자열 포맷 인자 (예: cols="사번, 부서")

    Returns:
        번역된 문자열. 키가 없으면 key 자체를 반환.

    Example:
        >>> t("app_title", "en")
        'HR Statistics Dashboard'
        >>> t("error_column_missing", "ko", cols="사번, 부서")
        '필수 컬럼을 찾을 수 없습니다: 사번, 부서'
    """
    lang_dict = TRANSLATIONS.get(lang, TRANSLATIONS["ko"])
    text = lang_dict.get(key)

    # fallback: lang에 없으면 ko에서 찾기
    if text is None:
        text = TRANSLATIONS["ko"].get(key, key)

    return text.format(**kwargs) if kwargs else text


